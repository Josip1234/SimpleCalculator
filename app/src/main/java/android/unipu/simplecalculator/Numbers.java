package android.unipu.simplecalculator;

public class Numbers {
    private double mNumberOne;
    private double mNumberTwo;

    public Numbers(double mNumberOne, double mDoubleTwo) {
        this.mNumberOne = mNumberOne;
        this.mNumberTwo = mDoubleTwo;
    }

    public double getmNumberOne() {
        return mNumberOne;
    }

    public void setmNumberOne(double mNumberOne) {
        this.mNumberOne = mNumberOne;
    }

    public double getmNumberTwo() {
        return mNumberTwo;
    }

    public void setmNumberTwo(double mNumberTwo) {
        this.mNumberTwo = mNumberTwo;
    }
}
